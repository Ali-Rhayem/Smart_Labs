class User {
  final String id;
  final String name;
  final String email;
  final String password;
  final String major;
  final String faculty;
  final String imageUrl;
  final String role;
  final String faceIdentityVector;

  User({
    required this.id,
    required this.name,
    required this.email,
    required this.password,
    required this.major,
    required this.faculty,
    required this.imageUrl,
    required this.role,
    required this.faceIdentityVector,
  });
}
